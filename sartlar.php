<h1>ekşi sözlük kullanıcı sözleşmesi</h1>
<div id="yazi">
    ekşi sözlük, kayıtlı kullanıcılarının içeriklerine yer sağlayıcılık hizmeti veren bir internet sitesi olup, ekşi
    teknoloji ve bilişim anonim şirketi’ne (ekşi teknoloji) aittir.
    ekşi sözlük, kayıtlı kullanıcılarının içeriklerine yer sağlayıcılık hizmeti veren bir internet sitesi olup, ekşi
    teknoloji ve bilişim anonim şirketi’ne (ekşi teknoloji) aittir.

    ekşi sözlük(site) kayıtlı kullanıcısı olduğunuzda tabi olduğunuz kurallar ve diğer yasal konular aşağıdaki gibidir.

    - kayıtlı kullanıcı ve yazar olmak

    18 yaş sınırı uygulamaktayız, bu nedenle 18 yaşından küçüklerin kullanıcı olması maalesef mümkün değil.

    ekşi sözlük’te kayıtlı kullanıcı olmak için sizden istenilen bilgileri girmeniz gerekmekte. bu işlem tamamlandıktan
    sonra size aktivasyon için eposta göndereceğiz.

    hesabınız aktive olduktan sonra oluşturacağınız içerikler (entryler) yazarlığınız onaylanana kadar sitede
    görünmeyecektir ancak bu içeriklere farklı kanallardan erişim sağlayabiliriz.

    yazarlığınızın onaylanması ekşi sözlük’ün takdirinde olup, kayıtlı kullanıcı olmanız yazar olabileceğiniz anlamına
    gelmemektedir. kayıtlı kullanıcıların yazarlığa alınmasındaki değerlendirme kriterleri, tercih edilecek yöntemler
    veya süreye ilişkin de hiçbir taahhütte bulunmamaktayız. yazar alımları sürekli yapılmakla beraber, belli dönemlerde
    belli kriterleri karşılayanların yazarlığa alınması gibi ek uygulamalarımız da olabilmektedir.

    - işleyiş ve sorumluluklarınız

    oluşturduğunuz içerik ekşi sözlük kurallarına ve hukuka uygun olmak zorundadır. oluşturduğunuz içerik tüm
    sorumluluğu size ait olmak üzere ve hiçbir ön denetime tabi olmadan yayına girdiğinden, oluşturduğunuz içeriğin
    hukuka aykırı olmadığından lütfen emin olun. içerik yayına alındığı anda hukuki sorumluluğunuz başladığından,
    oluşturduğunuz içeriğin daha sonra tarafınızca veya şikayet/haberdar edilme vs. üzerine ekşi sözlük tarafından
    silinmiş olması sorumluluğunuzu ortadan kaldırmayacaktır.

    ekşi sözlük kuralları, entrylerin silinme sebepleri ve diğer yaptırımlar kullanıcılara site üzerinden
    duyurulmaktadır, bunların takibi sizin sorumluluğunuzdadır.

    entryleriniz ekşi sözlük kurallarına uygun olmaması nedeniyle veya hukuka aykırı olması sebebiyle yayından
    kaldırılabilir. kurallara veya hukuka aykırılıklar nedeniyle yazarlığınız geçici olarak durdurulabilir veya
    kullanıcı hesabınız kapatılabilir. diğer tüm haklarımız saklıdır.

    gerekli gördüğümüz takdirde ve hiçbir gerekçe göstermeksizin dilediğimiz içeriği kısmen veya tamamen yayından
    kaldırma, yazarlığınızı iptal etme, kullanıcı kaydınızı silme haklarını saklı tutmaktayız.

    hesabınızın güvenliğine ilişkin tüm sorumluluk size aittir. şifrenizi kolay tahmin edilemeyecek şekilde
    oluşturmanızı ve üçüncü kişilerle paylaşmamanızı, üçüncü kişilere ait web sitelerine ve/veya mobil uygulamalara
    kullanıcı adı ve şifrenizi girmemenizi tavsiye ederiz.

    ekşi sözlük’e vermiş olduğunuz bilgilerinizin güvenliği için ekşi sözlük elinden gelen özeni göstermektedir ancak
    ekşi sözlük’ün özen yükümlülüğünden başka yükümlülüğünün olmadığını, barındırma hizmeti ve benzeri hizmet veren
    firmaların güvenliği sağlayamaması veya ekşi sözlük’e yasadışı olarak erişilerek bilgilerin ele geçirilmesi gibi
    hallerde doğrudan ya da dolaylı hiçbir zarara ilişkin sorumluluk kabul etmemekteyiz.

    her ne kadar düzenli olarak yedekleme yapıyor olsak da, sitedeki verilerinizin (entryler, mesajlar, notlar ve
    benzeri her türlü içerik) kısmen veya tamamen kaybolmayacağına ilişkin hiçbir taahhütte bulunmamaktayız. ayrıca bu
    verileri kalıcı olarak silme hakkını da saklı tutmaktayız. bu nedenle herhangi bir sebeple yaşanacak veri kaybı veya
    bozulması halinde hiçbir şekilde sorumluluk kabul etmemekteyiz. veri kaybı yaşama gibi bir endişeniz varsa ekşi
    sözlük’teki verilerinizi kendi imkanlarınızla yedeklemenizi öneririz.

    - gizlilik

    ekşi sözlük’te bulunduğunuz süre boyunca 'cookie' olarak da adlandırılan çerezlerin ve buna benzer unsurların
    bilgisayarınıza yerleştirilmesi söz konusu olabilir. çerezler basit metin dosyalarından ibaret olup, kimlik ve sair
    özel bilgiler içermez, bu nevi kişisel bilgi içermemekle beraber, oturum bilgileri ve benzeri veriler saklanır ve
    sizi tekrar tanımak için kullanılabilir. bu konuda daha fazla bilgiyi http://en.wikipedia.org/wiki/http_cookie ve
    http://tr.wikipedia.org/wiki/çerez adreslerinden edinebilirsiniz. (verilen linklerden ulaşacağınız içeriğin
    güvenilirliğine ilişkin sorumluluğumuz bulunmamaktadır.)

    ayrıca ekşi sözlük'e kaydolmanız esnasında ve kullanımınız süresince sizden doğrudan talep ettiğimiz(haberli) veya
    siteyi kullanımınız esnasında yaptığınız işlemlerin otomatik olarak kaydedilmesi(habersiz) şeklinde tarafınızdan
    toplanılan her türlü veriyi süre sınırı olmaksızın saklama, işleme ve kullanma hakkımız bulunmaktadır ancak bu
    sözleşmede aksi belirtilen durumlar saklıdır.

    sizden topladığımız veriler gizli tutulacaktır. bu veriler ekşi teknoloji'nin geliştirme çalışmalarında
    kullanılabilecek, anonimiteniz korunarak istatistiki veri olarak kullanılabilecek ve anonimiteniz korunmak şartıyla
    üçüncü kişilerle paylaşılabilecektir.

    iletişim bilgileriniz sizden aksi yönde onay alınmadıkça sadece ekşi teknoloji'ye ait marka ve/veya hizmetlere
    ilişkin bilgilendirme ve tanıtım amacıyla kullanılacaktır, iletişim bilgileriniz hiçbir şekilde üçüncü kişilere
    kullandırılmayacak veya verilmeyecektir.

    yasal zorunluluğun söz konusu olması ve yetkili makamlar tarafından usulüne uygun olarak talep edilmesi halinde
    bilgileriniz ilgili makamla paylaşılabilecektir. yürürlükteki mevzuat gereği tüm kayıtlı kullanıcıların sadece son
    12 aylık ip bilgisi saklanmaktadır.

    kendi isteğiniz doğrultusunda mesajlaşma yoluyla paylaştığınız veya entrylerinizle alenileştirdiğiniz bilgilerinize
    ilişkin olarak ekşi sözlük’ün hiçbir sorumluluğu bulunmamaktadır. kişisel yazışma yoluyla veya yayınladığınız
    entrylerde paylaştığınız bilgilere özen göstermenizi tavsiye ederiz.

    - telif

    ekşi sözlük’te oluşturduğunuz/yayınladığınız içeriklerin telif hakları size aittir. bu nedenle ekşi sözlük’te
    yayınlandığınız içeriğinizi (entrylerinizi) ticari maksatla dahi dilediğiniz şekilde derleyip, kullanabilirsiniz.
    ancak ekşi sözlük'te yayında olan içeriklerinizi internet üzerinde başka bir sitede daha yayınlamanız halinde ekşi
    sözlük’e aktif link vermeniz gerekmektedir. ekşi, sourtimes, ekşi şeyler ve ekşi sözlük markalarının kullanımı ise
    ekşi teknoloji’nin yazılı iznine tabidir.

    telif hakları size ait olmakla birlikte, ekşi sözlük’te yayınladığınız tüm içerik için ekşi teknoloji’ye çoğaltma,
    kopyalama, işleme ve basılı ortam, radyo-televizyon, uydu ve kablo gibi telli veya telsiz yayın yapan kuruluşlar
    vasıtasıyla veya internet ve sair dijital iletim şekilleri de dahil olmak üzere işaret, ses ve/veya görüntü nakline
    yarayan araçlar ve sair her türlü araçla yayınlama hakları dahil olmak ancak bunlarla kısıtlı kalmamak kaydıyla tüm
    mali haklarını hiçbir coğrafi sınırlama olmaksızın, süresiz ve bedelsiz kullanma hakkı(lisans) vermektesiniz.

    oluşturduğunuz içerikler ekşi sözlük’te yayında olduğu müddetçe ekşi teknoloji bu içerikleri başta ekşi şeyler'de
    olmak üzere, uygun gördüğü bütün ortamlarda kendi kullanımında olan markalar altında ve/veya diğer internet
    sitelerinde ticari amaçla kullanma hakkına sahip olacaktır. bunu engellemek için kullanılmasını istemediğiniz
    içeriği silmeniz veya hesabınızı kapamanız yeterlidir ancak bu eylemler sadece ileriye dönük olup, size daha önce
    yapılmış çalışmalardan entrylerinizin çıkarılması, telif ücreti talep etme ve benzeri bir hak vermeyecektir. daha
    önce yapılmış çalışmaların yeniden basımı veya yayını da bu nedenle her zaman mümkündür.

    ekşi teknoloji, entrylerinizin internet dışındaki ortamlarda ticari amaçla kullanılması halinde, ilgili çalışmadan
    kar elde edilmesi şartıyla, kendi takdir edeceği miktarda telif ödemesinde bulunacağını taahhüt eder.

    ekşi sözlük’te yayınladığınız içeriğin size ait olduğu veya fikir ve sanat eserleri kanunu çerçevesinde kullanma
    hakkınız olduğu kabul edilir. size ait olmayan içeriğin ancak kanunlarda belirtilen usule uygun olarak ve atıfta
    bulunmanız şartıyla yayınlanması mümkündür. aksi halde uğrayabileceğimiz zararlardan sorumlu olacağınızı belirtmek
    isteriz.

    - sub-etha ve diğer fonksiyonlar

    sözlük içi mesajlaşma ve diğer imkanları kullanırken hukuka ve varsa bunlara ilişkin kurallara uygun davranmanız
    gerekmektedir.

    sub-etha bölümünde yer alan siteler -aksi yazılı olarak açıkça belirtilmedikçe- tamamen üçüncü kişilere ait olup,
    geliştirilmesi, işletilmesi ve yönetilmesi tamamen kendilerince yapılmakta olup, ekşi teknoloji’nin bu sitelerle
    hiçbir bağlantısı bulunmamaktadır. bu sitelerin işleyişi, kuralları, veri güvenliği prosedürleri ve gizlilik
    politikaları hakkında hiçbir bilgimiz, teminatımız veya taahhüdümüz bulunmamaktadır.

