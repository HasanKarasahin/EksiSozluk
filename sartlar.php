    (bkz: ekşi sözlük kullanım koşulları/#19784395) bu sözleşmenin ayrılmaz bir parçasıdır.

    - sözleşme değişiklikleri

    bu sözleşme ekşi teknoloji tarafından önceden duyurulmaksızın ve tek taraflı olarak değiştirilebilir. değişiklik
    sonrası sitenin kayıtlı kullanıcı olarak kullanım sözleşme değişikliklerinin kabulü anlamına geleceğinden sözleşme
    metnini belirli aralıklarla kontrol etmenizi tavsiye ederiz.

    bu sözleşmesel ilişki nedeniyle taraflar arasında doğabilecek ihtilaflarda ekşi sözlük’ün kayıtlarının(eposta
    yazışmaları, internet trafik bilgisi ve erişim kayıtları[log] vs.) hmk 193. maddesi anlamında geçerli, bağlayıcı,
    kesin ve münhasır delil olacağını, bu maddenin delil sözleşmesi niteliğinde olduğunu kabul etmektesiniz.

    bu sözleşmeye türk hukuku uygulanacak olup, ihtilafların hallinde istanbul merkez (çağlayan) mahkeme ve icra
    daireleri yetkilidir.

    bu sözleşme en son 26.01.2016 tarihinde güncellenmiştir.
